const translations: Record<SupportedLocale, TranslationTable> = { en, no, sv };

export function supportedLocale(locale: string | undefined): SupportedLocale {
  const language = locale?.trim().toLowerCase().split(/[\-_]/u)[0];
  if (language === "sv") return "sv";
  if (language === "no" || language === "nb" || language === "nn") return "no";
  return "en";
}

export function translate(
  locale: SupportedLocale,
  key: TranslationKey,
  values: Readonly<Record<string, string | number>> = {},
): string {
  return Object.entries(values).reduce(
    (message, [name, value]) => message.replace(`{${name}}`, String(value)),
    translations[locale][key],
  );
}

export type { TranslationKey };
